package main

// Level 은 하나의 Vim 버퍼(텍스트 맵)와 학습 목표를 표현한다.
// 게임 보드는 곧 Vim 버퍼다 — 커서가 실제 Vim처럼 텍스트 위를 움직인다.
//
// 맵 글자 범례:
//
//	@  플레이어(커서) 시작 위치
//	.  바닥(걸어다닐 수 있음)
//	K  열쇠 — 위로 이동하면 자동 획득
//	*  버그 — 그 위에서 x 를 눌러 제거
//	$  출구 — 모든 열쇠 획득 + 모든 버그 제거 후 도달하면 클리어
//	(공백) 빈 칸 — 단어 점프(w/b/e) 학습용 구분자
type Level struct {
	Title string   // 한국어 레벨 제목 (DOM 으로 전달)
	Hint  string   // 한국어 힌트 (DOM 으로 전달)
	Map   []string // 텍스트 버퍼
}

// W1 "이동의 숲" — MVP 3개 레벨.
